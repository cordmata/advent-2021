package main

import (
	"fmt"
	"log"
	"sort"
	"strings"
)

const p1Example = 26397
const p2Example = 288957

type stack struct {
	chars []rune
}

func (s *stack) push(r rune) {
	s.chars = append(s.chars, r)
}

func (s *stack) pop() rune {
	var out rune
	stacklen := len(s.chars) - 1
	out = s.chars[stacklen]
	s.chars = s.chars[:stacklen]
	return out
}

func (s *stack) empty() bool {
	return len(s.chars) <= 0
}

type parseResult struct {
	incomplete     stack
	unmatchedScore int
}

func parseLine(line string) parseResult {
	var stack stack
	var result parseResult
	for _, c := range line {
		if _, isStarter := chunkStarters[c]; isStarter {
			stack.push(c)
			continue
		}
		if info, isCloser := chunkClosers[c]; isCloser && !stack.empty() {
			potentialMatch := stack.pop()
			if potentialMatch != info.startingMatch {
				result.unmatchedScore = info.pointsUnmatched
				break
			}
		}
	}
	if result.unmatchedScore == 0 {
		result.incomplete = stack
	}
	return result
}

func part1(input []string) int {
	var score int
	for _, line := range input {
		r := parseLine(line)
		score += r.unmatchedScore
	}
	return score
}

func part2(input []string) int {
	var incomplete []stack
	for _, line := range input {
		r := parseLine(line)
		if !r.incomplete.empty() {
			incomplete = append(incomplete, r.incomplete)
		}
	}

	var completions [][]rune
	for _, s := range incomplete {
		var comp []rune
		for !s.empty() {
			starter := s.pop()
			if end, ok := chunkStarters[starter]; ok {
				comp = append(comp, end)
				continue
			}
			log.Fatalln("shouldn't make it here")
		}
		completions = append(completions, comp)
	}

	var scores []int
	for _, v := range completions {
		var score int
		for _, r := range v {
			endInfo := chunkClosers[r]
			score *= 5
			score += endInfo.pointsMatched
		}
		scores = append(scores, score)
	}
	sort.Ints(scores)
	return scores[len(scores)/2]
}

func processInput(s string) []string {
	return strings.Split(s, "\n")
}

type endChunkInfo struct {
	startingMatch   rune
	pointsUnmatched int
	pointsMatched   int
}

var chunkStarters = map[rune]rune{
	'(': ')',
	'[': ']',
	'{': '}',
	'<': '>',
}

var chunkClosers = map[rune]endChunkInfo{
	')': {'(', 3, 1},
	']': {'[', 57, 2},
	'}': {'{', 1197, 3},
	'>': {'<', 25137, 4},
}

func main() {
	exampleInput := processInput(exampleInput)
	actualInput := processInput(actualInput)

	if r := part1(exampleInput); r != p1Example {
		log.Fatalf("[PART 1]: expected %d, got %d", p1Example, r)
	}
	fmt.Println("The answer to part 1 is:", part1(actualInput))

	if r := part2(exampleInput); r != p2Example {
		log.Fatalf("[PART 2]: expected %d, got %d", p2Example, r)
	}
	fmt.Println("The answer to part 2 is:", part2(actualInput))
}

