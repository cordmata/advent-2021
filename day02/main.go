package main

import (
	"log"
	"strconv"
	"strings"
)

func main() {
	if response := part1(exampleInput); response != 150 {
		log.Fatalf("[ERROR] Part 1 should be 150, not %d", response)
	}
	log.Printf("The answer to part 1 is: %d", part1(actualInput))
	if response := part2(exampleInput); response != 900 {
		log.Fatalf("[ERROR] Part 2 should be 900, not %d", response)
	}
	log.Printf("The answer to part 2 is: %d", part2(actualInput))
}

type command struct {
	direction string
	unit      int
}

type state1 struct {
	x int
	y int
}

type state2 struct {
	state1
	aim int
}

func (s *state1) processCommand(c command) {
	switch c.direction {
	case "forward":
		s.x += c.unit
	case "down":
		s.y += c.unit
	case "up":
		s.y -= c.unit
	}
}

func (s *state2) processCommand(c command) {
	switch c.direction {
	case "forward":
		s.x += c.unit
		s.y += s.aim * c.unit
	case "down":
		s.aim += c.unit
	case "up":
		s.aim -= c.unit
	}
}

func part1(commands []command) int {
	state := &state1{}
	for _, c := range commands {
		state.processCommand(c)
	}
	return state.x * state.y
}

func part2(commands []command) int {
	state := &state2{}
	for _, c := range commands {
		state.processCommand(c)
	}
	return state.x * state.y
}

func processInput(input string) []command {
	commandStrings := strings.Split(input, "\n")
	var commands []command
	for _, cmd := range commandStrings {
		parts := strings.Split(cmd, " ")
		unit, _ := strconv.Atoi(parts[1])
		commands = append(commands, command{
			direction: parts[0],
			unit:      unit,
		})
	}
	return commands
}

